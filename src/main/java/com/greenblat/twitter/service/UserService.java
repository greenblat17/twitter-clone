package com.greenblat.twitter.service;

import com.greenblat.twitter.model.Role;
import com.greenblat.twitter.model.User;
import com.greenblat.twitter.repo.UserRepo;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.security.core.userdetails.UserDetails;
import org.springframework.security.core.userdetails.UserDetailsService;
import org.springframework.security.core.userdetails.UsernameNotFoundException;
import org.springframework.stereotype.Service;
import org.springframework.util.StringUtils;

import java.util.Collections;
import java.util.UUID;

@Service
public class UserService implements UserDetailsService {
    @Autowired
    private UserRepo userRepo;

    @Autowired
    private MailSenderService mailSender;

    @Override
    public UserDetails loadUserByUsername(String username) throws UsernameNotFoundException {
        return userRepo.findByUsername(username);
    }

    public boolean addUser(User user) {
        User userFromDatabase = userRepo.findByUsername(user.getUsername());

        if (userFromDatabase != null) {
            return false;
        }

        user.setActive(false);
        user.setRoles(Collections.singleton(Role.USER));
        user.setActivationCode(UUID.randomUUID().toString());
        userRepo.save(user);

        if (!user.getEmail().isEmpty()) {
            String text = String.format("Hello %s! Welcome to Clone Twitter. Please visit this link: http://localhost:8080/activate/%s",
                    user.getUsername(), user.getActivationCode());
            mailSender.sendSimpleMessage(user.getEmail(), "Activation code", text);
        }

        return true;
    }

    public boolean activateUser(String code) {
        User user = userRepo.findByActivationCode(code);

        if (user == null) {
            return false;
        }

        user.setActivationCode(null);
        user.setActive(true);

        userRepo.save(user);

        return true;
    }
}
